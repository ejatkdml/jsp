package bean;

public class CountBean {
	
	private int novelcnt;
	private int tagcnt;
	private int authorcnt;
	private int webcnt;
	private int pubcnt;
	private int charactercnt;
	private int traitcnt;

	public int getNovelcnt() {
		return novelcnt;
	}

	public void setNovelcnt(int novelcnt) {
		this.novelcnt = novelcnt;
	}

	public int getTagcnt() {
		return tagcnt;
	}

	public void setTagcnt(int tagcnt) {
		this.tagcnt = tagcnt;
	}

	public int getAuthorcnt() {
		return authorcnt;
	}

	public void setAuthorcnt(int authorcnt) {
		this.authorcnt = authorcnt;
	}

	public int getWebcnt() {
		return webcnt;
	}

	public void setWebcnt(int webcnt) {
		this.webcnt = webcnt;
	}

	public int getPubcnt() {
		return pubcnt;
	}

	public void setPubcnt(int pubcnt) {
		this.pubcnt = pubcnt;
	}

	public int getCharactercnt() {
		return charactercnt;
	}

	public void setCharactercnt(int charactercnt) {
		this.charactercnt = charactercnt;
	}

	public int getTraitcnt() {
		return traitcnt;
	}

	public void setTraitcnt(int traitcnt) {
		this.traitcnt = traitcnt;
	}

}
